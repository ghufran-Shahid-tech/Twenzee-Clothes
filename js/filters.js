// Helpers
const getProductById = (id) => products.find(p => p.id === parseInt(id));
const getProductsByCategory = (cat) => cat === 'all' ? products : products.filter(p => p.category === cat);
const getTrendingProducts = () => products.filter(p => p.isTrending).slice(0,8);
const getNewArrivals = () => products.filter(p => p.isNew).slice(0,8);
const formatPrice = (price) => new Intl.NumberFormat('en-PK',{style:'currency',currency:'PKR',minimumFractionDigits:0}).format(price);
const searchProducts = (query) => {
  const q = query.toLowerCase();
  return products.filter(p => p.name.toLowerCase().includes(q) || p.category.toLowerCase().includes(q) || p.description.toLowerCase().includes(q));
};

